package Calculadora;

public class Operaciones {

    //Overload
    int sumar(int num1, int num2){

        return num1 + num2;
    }

    double sumar(double num1, double num2){

        return num1 +  num2;
    }

    int restar(int num1, int num2){

        return num1 - num2;
    }

    double restar(double num1, double num2){

        return num1 - num2;
    }








}
